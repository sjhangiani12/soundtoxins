<p class="bigtitle">About SoundToxins</p>

<p>SoundToxins, a diverse partnership of Washington state shellfish and finfish growers, environmental learning centers, Native tribes, and Puget Sound volunteers, is a monitoring program designed to provide early warning of harmful algal blooms and <em>Vibrio parahaemolyticus</em> events in order to minimize both human health risks and economic losses to Puget Sound fisheries.</p>

<p class="bigtitle">Project Goals</p>
<p>The overall goal of this cooperative partnership is to establish a cost-effective monitoring program that will be led by state managers, tribal harvesters, and commercial fish and shellfish farmers. The SoundToxins program aims to provide sufficient warning of HAB and vibrio events to enable early or selective harvesting of seafood, thereby minimizing risks to human health and reducing economic losses to Puget Sound fisheries. The objectives of the SoundToxins program are: 1) to determine which environmental conditions promote the onset and flourishing of HABs and increased concentrations of <em>V. parahaemolyticus</em>; and 2) to determine which combination of environmental factors can be used for early warning of these events. To accomplish this, seawater samples are collected weekly by the participants at 11 different sites throughout Puget Sound and are analyzed for salinity, temperature, nutrients, chlorophyll, (paralytic shellfish toxins and domoic acid) phytoplankton species, and <em>V. parahaemolyticus</em>. Phytoplankton species diversity is described and the four target species specifically identified and enumerated are <em>Pseudo-nitzschia</em> species, <em>Alexandrium catenella</em>, <em>Dinophysis</em> species, and <em>Heterosigma akashiwo</em>.</p>

